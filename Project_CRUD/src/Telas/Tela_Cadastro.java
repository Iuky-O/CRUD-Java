    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String nome = jTextField1.getText();
        String especificacoes = jTextField2.getText();
        String precoCusto = jTextField4.getText();
        String precoVenda = jTextField3.getText();
        
        int itemSelecionado = jComboBox2.getSelectedIndex();
        
        float pv = 0;
        float pc = 0;
        
        try{
            pv= Float.parseFloat(precoVenda);
            pc= Float.parseFloat(precoCusto);
            
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(this, "Preço inválido");
            
        }
        
        Produto p = new Produto(nome,especificacoes,pv,pc,itemSelecionado != 0 ? false:true);
        
        Cadastro_Controle controlador = new Cadastro_Controle();
        
        if(controlador.cadastrarProduto(p)){
            
            p.addL();
            JOptionPane.showMessageDialog(this,"Cadastro com sucesso");
            
            this.dispose();
            
            new Tela_Cadastro().setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(this,"Cadastro não realizado \n\n" + "Faltando dados");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

